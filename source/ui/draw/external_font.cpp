void Exfont_text_sort(u64 *source_string, int characters) {
	int reverse_start = -1;
	for (int i = 0; i < characters; i++) {
		bool is_right_to_left = false;
		
		if (exfont_font_right_to_left_samples[201] == source_string[i]) is_right_to_left = true;
		else if (source_string[i] > right_to_left_sample[0] && source_string[i] < right_to_left_sample[1]) {
			for (int j = 0; j < exfont_num_of_right_left_charcters - 1; j++) {
				if (source_string[i] == exfont_font_right_to_left_samples[j]) {
					is_right_to_left = true;
					break;
				}
			}
		}
		
		if (is_right_to_left && reverse_start == -1) reverse_start = i; // start of reverse characters
		if (!is_right_to_left && reverse_start != -1) { // end of reverse characters
			std::reverse(source_string + reverse_start, source_string + i);
			reverse_start = -1;
		}
	}
	if (reverse_start != -1) std::reverse(source_string + reverse_start, source_string + characters);
}

void Exfont_text_parse(std::string source_string, u64 *out_strings, int max_loop, int* out_element) {
	int source_length = source_string.size();
	int out_num = 0;
	
	int source_head = 0;
	for (int i = 0; i < max_loop && source_head < source_length; i++) {
		int cur_length = mblen(&source_string[source_head], 4);
		if (cur_length >= 1) {
			u64 cur_char = 0;
			for (int j = source_head; j < source_head + cur_length; j++) cur_char = cur_char * 0x100 + (u8) source_string[j];
			out_strings[out_num++] = cur_char;
			source_head += cur_length;
		} else source_head++;
	}
	out_strings[out_num] = 0;
	*out_element = out_num;
}

void Exfont_draw_external_fonts(u64 *in_string, size_t length, float texture_x, float texture_y, float texture_size_x, float texture_size_y, int abgr8888, float* out_width, float* out_height)
{
	static const double interval_offset = 0.5;
	double x_offset = 0.0;
	double y_offset = 0.0;
	
	for (size_t s = 0; s < length; s++) {
		u64 cur_char = in_string[s];
		
		if (cur_char == exfont_font_samples[0]) break; // NULL
		
		int block = -1;
		bool unknown = true;
		int char_size = 0;
		if (cur_char < 0x100) {
			char_size = 1;
			if (cur_char < sample_one_byte[0]) block = 0;
		} else if (cur_char < 0x10000) {
			char_size = 2;
			for (int i = 0; i < 10; i++) {
				if (cur_char < samples_two_bytes[i]) {
					block = i + 1;
					break;
				}
			}
		} else if (cur_char < 0x1000000) {
			char_size = 3;
			for (int i = 0; i < 37; i++) {
				if (cur_char < samples_three_bytes[i]) {
					block = i + 11;
					break;
				}
			}
		} else if (cur_char < 0x100000000ull) {
			char_size = 4;
			for (int i = 0; i < 2; i++) {
				if (cur_char < samples_four_bytes[i]) {
					block = i + 48;
					break;
				}
			}
		}
		
		if (block == 25 || block == 46) {
			if (std::find(std::begin(ignore_chars), std::end(ignore_chars), cur_char) != std::end(ignore_chars)) {
				unknown = false;
				block = -1;
			}
		}


		if (block != -1 && exfont_loaded_external_font[block] && char_size >= 1 && char_size <= 4) {
			// binary search
			auto *start = exfont_font_samples + exfont_font_start_num[block];
			auto *end = start + exfont_font_characters[block];
			auto *ptr = std::lower_bound(start, end, cur_char);
			if (ptr < end && *ptr == cur_char) {
				unknown = false;
				double x_size = (exfont_font_interval[ptr - exfont_font_samples] + interval_offset) * texture_size_x;
				Draw_texture(exfont_font_images[ptr - exfont_font_samples], abgr8888, (texture_x + x_offset), (texture_y + y_offset), x_size, 20.0 * texture_size_y);
				x_offset += x_size;
			}
		}

		if (unknown) {
			double x_size = (exfont_font_interval[0] + interval_offset) * texture_size_x;
			Draw_texture(exfont_font_images[0], abgr8888, (texture_x + x_offset), (texture_y + y_offset), x_size, 20.0 * texture_size_y);
			x_offset += x_size;
		}
	}
	
	*out_width = x_offset;
	*out_height = y_offset;
}


float Exfont_get_width_one(u64 cur_char, float texture_size_x)
{
	const double interval_offset = 0.5;
	int char_size = 0;
	
	int block = -1;
	
	if (cur_char < 0x100) {
		char_size = 1;
		if (cur_char < sample_one_byte[0]) block = 0;
	} else if (cur_char < 0x10000) {
		char_size = 2;
		for (int i = 0; i < 10; i++) {
			if (cur_char < samples_two_bytes[i]) {
				block = i + 1;
				break;
			}
		}
	} else if (cur_char < 0x1000000) {
		char_size = 3;
		for (int i = 0; i < 37; i++) {
			if (cur_char < samples_three_bytes[i]) {
				block = i + 11;
				break;
			}
		}
	} else if (cur_char < 0x100000000ull) {
		char_size = 4;
		for (int i = 0; i < 2; i++) {
			if (cur_char < samples_four_bytes[i]) {
				block = i + 48;
				break;
			}
		}
	}

	if ((block == 25 || block == 46) && std::find(std::begin(ignore_chars), std::end(ignore_chars), cur_char) != std::end(ignore_chars)) return 0;
	
	if (block != -1 && exfont_loaded_external_font[block] && char_size >= 1 && char_size <= 4) {
		// binary search
		auto *start = exfont_font_samples + exfont_font_start_num[block];
		auto *end = start + exfont_font_characters[block];
		auto *ptr = std::lower_bound(start, end, cur_char);
		if (ptr < end && *ptr == cur_char) return (exfont_font_interval[ptr - exfont_font_samples] + interval_offset) * texture_size_x;
	}
	return (exfont_font_interval[0] + interval_offset) * texture_size_x;
}
float Exfont_get_width(std::string in_string, float texture_size_x)
{
	int characters = 0;
	std::vector<u64> multibyte_chars(in_string.size() + 1);
	Exfont_text_parse(in_string, &multibyte_chars[0], in_string.size(), &characters);
	float res = 0;
	for (int s = 0; s < characters; s++) res += Exfont_get_width_one(multibyte_chars[s], texture_size_x);
	return res;
}

Result_with_string Exfont_load_exfont(int exfont_num)
{
	Result_with_string result;
	if (exfont_num >= 0 && exfont_num < DEF_EXFONT_NUM_OF_FONT_NAME)
	{
		result = Draw_load_texture("romfs:/gfx/font/" + exfont_font_name[exfont_num] + "_font.t3x", exfont_num + 5, exfont_font_images, exfont_font_start_num[exfont_num], exfont_font_characters[exfont_num]);

		if (result.code == 0)
		{
			for (int i = 0; i < exfont_font_characters[exfont_num]; i++)
			{
				C3D_TexSetFilter(exfont_font_images[exfont_font_start_num[exfont_num] + i].tex, GPU_LINEAR, GPU_LINEAR);
				C3D_TexSetWrap(exfont_font_images[exfont_font_start_num[exfont_num] + i].tex, GPU_CLAMP_TO_EDGE, GPU_CLAMP_TO_EDGE);
			}
			exfont_loaded_external_font[exfont_num] = true;
		}

		if (result.code != 0)
		{
			Exfont_unload_exfont(exfont_num);
			exfont_loaded_external_font[exfont_num] = false;
		}
	}
	else
	{
		result.code = DEF_ERR_INVALID_ARG;
		result.string = "Invalid exfont num";
	}

	return result;
}

void Exfont_unload_exfont(int exfont_num)
{
	if (exfont_num >= 0 && exfont_num < DEF_EXFONT_NUM_OF_FONT_NAME)
	{
		Draw_free_texture(5 + exfont_num);
		for (int j = exfont_font_start_num[exfont_num]; j < exfont_font_characters[exfont_num]; j++)
			exfont_font_images[j].tex = NULL;

		exfont_loaded_external_font[exfont_num] = false;
	}
}
